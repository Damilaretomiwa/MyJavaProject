public class Counter {
    public static void main(String[] args) {
        int counter = 0;
        while (counter <= 20) {
            System.out.println(counter);
            counter += 1;
        }
        counter = 2;
        while (counter < 2000) {
            System.out.println(counter);
            counter += 2;
        }
        counter = 3;
        while (counter < 1000) {
            System.out.println(counter);
            counter += 2;
        }
        counter = 10;
        while (counter < 500) {
            System.out.println(counter);
            counter += 10;
        }
        counter = 100;
        while (counter < 1000) {
            System.out.println(counter);
            counter += 100;
        }
    }
}











